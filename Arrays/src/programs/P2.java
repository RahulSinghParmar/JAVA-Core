package programs;

public class P2
{
    // W.J.P using loop in array

    public static void main(String[] args)
    {
        // Create an array
        int[] age = {12, 13, 14, 15};

        // loop through the array  using for loop
        System.out.println("using for loop\n");
        for (int i = 0; i < age.length; i++ )
        {
            System.out.println(age[i]);
        }
    }
}
